#include <iostream>
using namespace std;

int main()
{
    long long num_0, num_1;

    cin >> num_0 >> num_1;

    cout << num_0 << " + " << num_1 << " = " << num_0 + num_1 << "\n";
    cout << num_0 << " * " << num_1 << " = " << num_0 * num_1 << "\n";
    cout << num_0 << " - " << num_1 << " = " << num_0 - num_1 << endl;

    return 0;
}
